'''
Exercício - 13
Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento.
'''

salarioAntigo = float(input('Digite o valor de seu salário atual: '))
salarioNovo = salarioAntigo * 1.15
print(f'Após um aumento de 15% em seu salário seu salario será de {salarioNovo}')